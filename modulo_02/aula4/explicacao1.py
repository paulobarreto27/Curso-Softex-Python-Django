# Criando função
# 'def' define a função

def diga_oi():
    print("Oi! Tudo bemW")


# Para rodar uma função criada (def), devemos "chamar" a função.
diga_oi()

# 'nome' é um parâmetro
def saudar (nome):
    print(f"Olá, {nome}!")

# Passamos "Maria" para o parâmetro 'nome'
saudar("Maria")
saudar("João")